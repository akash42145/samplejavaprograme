package com.sample.mapEqualshash;

public class PersonalDetail {
	
	private long phone;
	private String city;
	
	
	public PersonalDetail(long phone, String city) {
		super();
		this.phone = phone;
		this.city = city;
	}
	public long getPhone() {
		return phone;
	}
	public void setPhone(long phone) {
		this.phone = phone;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	
	

}
